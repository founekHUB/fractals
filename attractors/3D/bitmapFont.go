type BitmapFont struct {
	surface    *sdl.Surface
	data       *FontMask
	maskSize   int32
	charWidth  int32
	charHeight int32
}

func NewBitmapFont(primarySurface *sdl.Surface, fontData *FontMask, fontMaskSize int32, fontCharWidth int32, fontCharHeight int32) *BitmapFont {
	font := BitmapFont{
		surface:    nil,
		data:       fontData,
		maskSize:   fontMaskSize,
		charWidth:  fontCharWidth,
		charHeight: fontCharHeight,
	}

	surface := font.createSurface(8, fontMaskSize)
	surface.Lock()
	font.renderAllBitmapCharacters(surface)
	surface.Unlock()
	surface.SetColorKey(true, 0x00ffffff)

	var err error
	font.surface, err = surface.Convert(primarySurface.Format, 0)
	if err != nil {
		panic(err)
	}
	defer surface.Free()

	return &font
}

func (font *BitmapFont) createSurface(width int32, height int32) *sdl.Surface {
	surface, err := sdl.CreateRGBSurface(sdl.SWSURFACE, width, height, 32, 0x00ff0000, 0x0000ff00, 0x000000ff, 0x00000000)
	if err != nil {
		panic(err)
	}
	return surface
}

func (font *BitmapFont) renderAllBitmapCharacters(surface *sdl.Surface) {
	pixels := surface.Pixels()
	p := 0
	for line := int32(0); line < font.maskSize; line++ {
		mask := *font.data
		scanline := mask[line]
		for x := 0; x < 8; x++ {
			color := byte(0xff)
			if scanline&0x80 != 0 {
				color = byte(0x00)
			}
			for i := 0; i < 3; i++ {
				pixels[p] = color
				p++
			}
			p++
			scanline = scanline << 1
		}
	}
}

func (font *BitmapFont) PrintCharacter(surface *sdl.Surface, x int, y int, ch byte) {
	ch -= 32
	if ch <= 0 {
		return
	}

	srcRect := sdl.Rect{
		X: 0,
		Y: font.charHeight * int32(ch),
		W: font.charWidth,
		H: font.charHeight,
	}
	dstRect := sdl.Rect{
		X: int32(x),
		Y: int32(y),
		W: 0,
		H: 0,
	}

	err := font.surface.Blit(&srcRect, surface, &dstRect)
	if err != nil {
		panic(err)
	}
}

func (font *BitmapFont) PrintString(surface *sdl.Surface, x int, y int, str string) {
	for i := 0; i < len(str); i++ {
		font.PrintCharacter(surface, x, y, str[i])
		x += int(font.charWidth)
	}
}
